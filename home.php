                    <p>LCB College was founded in 1971 as a night college by a group of teachers and operated from the premises of Maligaon Railway Higher Secondary School. At the initial stage, it catered mostly to undergraduate working men and women. It was at the initiative of the neighborhood community and the generous donation made by the family of Lalit Chandra Bharali that facilitated the purchase of its own land in 1972 and it was only from the next year that the college functioned for the first time from its own premises and since then it is the Governing Body that constitutes the primary management system of the college.</p>
                    <p> &nbsp;&nbsp; The establishment of the college was driven by a holistic approach towards higher education to the marginal student community who could not access the central/city colleges for various reasons. It was also an educational outreach for students who lived beyond the city limits and the entire Northeast. However, since the last two decades, the college has notched up its credibility as it attracts some of the best students from the entire northeast region for its non-traditional subjects at the UG level.</p>
